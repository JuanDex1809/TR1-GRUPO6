    public void busquedaLibro(){
        if(!"".equals(txtbuscadorLibros.getText())){
            List<Libro> listarLibro = libroDAO.buscarLibro(String.valueOf(cboTipo.getSelectedItem()) ,txtbuscadorLibros.getText());
            modeloTable = (DefaultTableModel) tableLibro.getModel();
            Object[] object = new Object[8];
            this.limpiarTablaLibro();
            for(Libro lib: listarLibro){
                object[0] = lib.getIdlibro();
                object[1] = lib.getAutorid();
                object[2] = lib.getAutor();
                object[3] = lib.getNombre();
                object[4] = lib.getFechapubli();
                object[5] = lib.getGeneroid();
                object[6] = lib.getGenero();
                object[7] = lib.getStock();
                modeloTable.addRow(object);
            }
            tableLibro.setModel(modeloTable);
            tableLibro.revalidate();
        }else{
            this.limpiarTablaLibro();
            this.listarLibro();
        }
    }
    
    public void buscarLibro(){
        txtbuscadorLibros.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                busquedaLibro();
                System.out.println(txtbuscadorLibros.getText());
            }
            
            @Override
            public void removeUpdate(DocumentEvent e) {
                busquedaLibro();
            }
            
            @Override
            public void changedUpdate(DocumentEvent e) {
            }
        });
    }
    
